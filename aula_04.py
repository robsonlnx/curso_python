# Tipos int e float
# int ->Número inteiro
# O tipo inteiro representa qualquer número
# positivo ou negativo. int sem sinal é considerado
# positivo.
print(11)
print(-11)
print(0)


# float -> Número com ponto flutuante 
# O tipo float representa qualquer número 
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
print(1.1, 10.11)
print(0.0, -1.5)


# A função type mostra o tipo que o python inferiou ao valor.
print(type('Robson'))
print(type(1))
print(type(1.2), type(-1.1), type(0.0))


