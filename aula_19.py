"""
Operadores de comparação (relacionais)
Op       Significado        Exemplo (True)
>        maior              2 > 1
>=       maoir ou igual     2 >= 2
<        menor              1 < 2
<=       menor ou igual     2 <= 2
==       igual              'a' == 'a'
!=       diferente          'a' != 'b'
"""

maior = 2 > 1
maoir_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'
print(maior)
print(maoir_ou_igual)
print(menor)
print(menor_ou_igual)
print(igual)
print(diferente)

